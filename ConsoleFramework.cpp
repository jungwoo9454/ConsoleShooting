void DrawBox(int x, int y, WORD color)
{
	if (x > 119 || x < 0 || y > 29 || y < 0)
		return;

	COORD coor = { x,y };
	DWORD dw;
	SetConsoleCursorPosition(hScreen[screenIndex], coor);
	SetConsoleTextAttribute(hScreen[screenIndex], color);

	WriteFile(hScreen[screenIndex], " ", 2, &dw, NULL);
}

void DrawChar(int x, int y, const char str, WORD color)
{
	if (x > 119 || x < 0 || y > 29 || y < 0)
		return;

	COORD coor = { x,y };
	DWORD dw;
	SetConsoleCursorPosition(hScreen[screenIndex], coor);
	SetConsoleTextAttribute(hScreen[screenIndex], color);

	char ch[2];
	ch[0] = str;
	ch[1] = '\0';

	WriteFile(hScreen[screenIndex], ch, 2, &dw, NULL);
}

void DrawStr(int x, int y, const char* str, WORD color)
{
	if (x > 119 || x < 0 || y > 29 || y < 0)
		return;

	COORD coor = { x,y };
	DWORD dw;
	SetConsoleCursorPosition(hScreen[screenIndex], coor);
	SetConsoleTextAttribute(hScreen[screenIndex], color);

	int maxLen = 120 - x;
	int strLen = strlen(str);

	if (maxLen < strLen)
		strLen = maxLen;

	WriteFile(hScreen[screenIndex], str, strLen, &dw, NULL);
}

void EngineSync(int fps)
{
	//					프로그램 실행 시간
	clock_t old = clock();
	clock_t cur;

	while (true)
	{
   		cur = clock();	// 0.03
		if (cur - old > 1000 / fps)
			break;
	}
}

void ClearScreen()
{
	COORD coor = { 0,0 };
	DWORD dw;

	FillConsoleOutputCharacter(hScreen[screenIndex], ' ', 120 * 30, coor, &dw);
	FillConsoleOutputAttribute(hScreen[screenIndex], 0, 120 * 30, coor, &dw);
}

void Flip()
{
	//더블 버퍼링
	SetConsoleActiveScreenBuffer(hScreen[screenIndex]);
	screenIndex = !screenIndex;
}

