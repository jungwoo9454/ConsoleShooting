{
	for (int i = 0; i < 30; i++)
	{
		for (int j = 0; j < 120; j++)
		{
			switch (screen[i][j])
			{
			case 1:
				DrawBox(j, i, WHITE_BOX);
				break;
			}
		}
	}

	char scoreText[30];
	sprintf_s(scoreText, "SCORE : %d", 0);
	DrawStr(1, 1, scoreText, WHITE);
}